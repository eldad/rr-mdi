scriptdir="$( cd "$( dirname "${BASH_SOURCE[0]}" )" ; pwd)"

target="${scriptdir}/../src/MdiType.re"

mdinames=$(mktemp)
mdicamel=$(mktemp)

switch_stmt() {
    paste "${mdicamel}" "${mdinames}" -d' ' | sed 's/\(.*\) \(.*\)/| \1 => "\2"/'
}

type_stmt() {
    sed 's/.*/| &/' < "${mdicamel}"
}

generate() {
    # shellcheck disable=SC2002
    cat "${scriptdir}/../node_modules/@mdi/font/css/materialdesignicons.css" | \
        sed -n '/^\.mdi-access/,/^\.mdi-blank/p' | \
        head -n -1 | grep '^\.' | tr -d '.' | \
        cut -d: -f1 > "${mdinames}"

    cat "${mdinames}" | sed 's/^mdi-//; s/^\([a-z]\)/\U\1/; s/-\([a-z0-9]\)/\U\1/g; s/^[0-9]/X&/' > "${mdicamel}"

    cat << EOM
/*
 *
 * Please do not edit manually.
 * This is an autogenerated file.
 *
 */

type t =
$(type_stmt)
;

let name = (kind: t) => {
    switch kind {
$(switch_stmt)
    }
};
EOM
}

generate > "${target}"
refmt "${target}" --in-place
